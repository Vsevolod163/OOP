package Lections.lection1.Ex2;

public class Point2D  {
    int x, y;

    public Point2D(int valueX, int valuY) {
        x = valueX;
        y = valuY;
    }
    public Point2D(){
        x = 0;
        y = 0;
    }
    // ctor - конструктор
    public Point2D(int value) {
        x = value;
        y = value;
    }
}
